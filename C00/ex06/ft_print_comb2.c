#include <unistd.h>

void	ft_write(char c[]);
void	ft_incr(char c2[]);
void	ft_chk_croiss(char c4[]);
void	ft_print_comb2(void);

void	ft_write(char c[4])
{
	write(1, &c[0], 1);
	write(1, &c[1], 1);
	write(1, " ", 1);
	write(1, &c[2], 1);
	write(1, &c[3], 1);
	if ((c[0] != '9') || (c[1] != '8') || (c[2] != '9') || (c[3] != '9'))
	{
		write(1, ",", 1);
		write(1, " ", 1);
	}
}

void	ft_incr(char c2[4])
{
	c2[3]++;
	if (c2[3] > '9')
	{
		c2[2]++;
		c2[3] = '0';
		if (c2[2] > '9')
		{
			c2[1]++;
			c2[2] = '0';
			if (c2[1] > '9')
			{
				c2[0]++;
				c2[1] = '0';
			}
		}
	}
}

void	ft_chk_croiss(char c4[4])
{
	if ((c4[1] == '9') && (c4[2] == c4[0]))
	{
		c4[3] = '0';
		c4[2] = c4[0] + 1;
	}
	if ((c4[3] < c4[1]) && (c4[2] < c4[0]))
	{
		if (c4[1] != '9')
			c4[3] = c4[1] + 1;
	}
	if (c4[2] < c4[0])
	{
		c4[2] = c4[0];
		if (c4[1] == '9')
			c4[2]++;
		if (c4[1] == c4[3])
			c4[3]++;
	}
}

void	ft_print_comb2(void)
{
	char	c3[4];

	c3[0] = '0';
	c3[1] = '0';
	c3[2] = '0';
	c3[3] = '0';
	while ((c3[0] < '9') || (c3[1] < '8') || (c3[2] < '9') || (c3[3] < '9'))
	{
		ft_incr(c3);
		ft_chk_croiss(c3);
		ft_write(c3);
	}
	write(1, "\n", 1);
}
