int	ft_strlen(char *str)
{	
	char	c;
	int		i_bcl;

	i_bcl = 0;
	c = str[0];
	while (c != '\0')
	{	
		i_bcl++;
		c = str[i_bcl];
	}
	return (i_bcl);
}
