char	*ft_strcpy(char *dest, char *src)
{
	int		i_bcl;

	i_bcl = 0;
	while (src[i_bcl] != '\0')
	{
		dest[i_bcl] = src[i_bcl];
		i_bcl++;
	}
	dest[i_bcl] = '\0';
	return (dest);
}
