char	*ft_strlowcase(char *str)
{
	int	i_bcl;

	i_bcl = 0;
	while (str[i_bcl] != '\0')
	{
		if ((str[i_bcl] >= 'A') && (str[i_bcl] <= 'Z'))
				str[i_bcl] += 32;
		i_bcl++;
	}
	return (str);
}
