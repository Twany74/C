int	ft_any(char **tab, int(*f)(char*))
{
	int	i;
	int	test;

	i = 0;
	test = 0;
	while (tab[i] != 0)
	{
		test = f(tab[i]);
		if (test != 0)
			return (1);
		i++;
	}
	return (0);
}
